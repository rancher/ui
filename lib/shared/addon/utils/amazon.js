export const nameFromResource = function(r, idField) {
  let id = r[idField];
  let out = id;

  if ( r && r.Tags && r.Tags.length ) {
    let match = r.Tags.filterBy('Key', 'Name')[0];

    if ( match ) {
      out = `${ match.Value  } (${  id  })`;
    }
  }

  return out;
}

export const tagsFromResource = function(r) {
  let out = [];

  if ( r && r.Tags && r.Tags.length ) {
    r.Tags.forEach((tag) => {
      if ( tag.Key !== 'Name' ) {
        out.push(`${ tag.Key }=${ tag.Value }`)
      }
    })
  }

  return out;
}


